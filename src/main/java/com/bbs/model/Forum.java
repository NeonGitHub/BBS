package com.bbs.model;

public class Forum {

	private int forumId;
	private String forumTitle;
	private String forumUserid;
	private String forumContent;
	private String forumType;
	private String forumTime;
	private int forumHeat;
	private int forumSort;

	public int getForumId() {
		return forumId;
	}

	public void setForumId(int forumId) {
		this.forumId = forumId;
	}

	public String getForumTitle() {
		return forumTitle;
	}

	public void setForumTitle(String forumTitle) {
		this.forumTitle = forumTitle;
	}

	public String getForumUserid() {
		return forumUserid;
	}

	public void setForumUserid(String forumUserid) {
		this.forumUserid = forumUserid;
	}

	public String getForumContent() {
		return forumContent;
	}

	public void setForumContent(String forumContent) {
		this.forumContent = forumContent;
	}

	public String getForumType() {
		return forumType;
	}

	public void setForumType(String forumType) {
		this.forumType = forumType;
	}

	public String getForumTime() {
		return forumTime;
	}

	public void setForumTime(String forumTime) {
		this.forumTime = forumTime;
	}

	public int getForumSort() {
		return forumSort;
	}

	public void setForumSort(int forumSort) {
		this.forumSort = forumSort;
	}

	public int getForumHeat() {
		return forumHeat;
	}

	public void setForumHeat(int forumHeat) {
		this.forumHeat = forumHeat;
	}


}
