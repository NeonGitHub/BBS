package com.bbs.model;

public class Moderator {

	private String modId;
	private String modName;
	private String modPwd;
	private String modSex;
	private String modBirth;
	private String modShow;
	private String modType;
	public String getModId() {
		return modId;
	}
	public void setModId(String modId) {
		this.modId = modId;
	}
	public String getModName() {
		return modName;
	}
	public void setModName(String modName) {
		this.modName = modName;
	}
	public String getModPwd() {
		return modPwd;
	}
	public void setModPwd(String modPwd) {
		this.modPwd = modPwd;
	}
	public String getModSex() {
		return modSex;
	}
	public void setModSex(String modSex) {
		this.modSex = modSex;
	}
	public String getModBirth() {
		return modBirth;
	}
	public void setModBirth(String modBirth) {
		this.modBirth = modBirth;
	}
	public String getModShow() {
		return modShow;
	}
	public void setModShow(String modShow) {
		this.modShow = modShow;
	}
	public String getModType() {
		return modType;
	}
	public void setModType(String modType) {
		this.modType = modType;
	}
	
	
	
	
}
