package com.bbs.model;

public class Manager {

	private String managerId;
	private String managerName;
	private String managerPwd;
	private String managerSex; 
	private String managerBirth;
	private String managerShow;
	public String getManagerId() {
		return managerId;
	}
	public void setManagerId(String managerId) {
		this.managerId = managerId;
	}
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public String getManagerPwd() {
		return managerPwd;
	}
	public void setManagerPwd(String managerPwd) {
		this.managerPwd = managerPwd;
	}
	public String getManagerSex() {
		return managerSex;
	}
	public void setManagerSex(String managerSex) {
		this.managerSex = managerSex;
	}
	public String getManagerBirth() {
		return managerBirth;
	}
	public void setManagerBirth(String managerBirth) {
		this.managerBirth = managerBirth;
	}
	public String getManagerShow() {
		return managerShow;
	}
	public void setManagerShow(String managerShow) {
		this.managerShow = managerShow;
	}
	
	
}
